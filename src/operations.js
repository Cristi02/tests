export const sum = (a, b) => {
    return a + b;
};

export const difference = (a, b) => {
    return a - b;
};

export const multiply = (a, b) => {
    return a * b;
};

export const division = (a, b) => {
    return a / b;
};

export const power = (a, b) => {
    return a ** b;
};

export const squaredRoot = (a) => {
    return Math.sqrt(a);
};
